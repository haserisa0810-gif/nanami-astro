import re


def fix_punctuation(text: str) -> str:
    """
    Gemini APIの出力テキストから不自然な読点を除去する。

    対象パターン：
    - 助詞（を・が・は・に・で・も・へ・と・や・の）直後の読点
    - ただし「と、」は列挙の可能性があるため除外
    - 「〜し、」の連用形接続は自然なケースが多いため除外
    """

    # ── 1. 助詞直後の読点を除去 ──────────────────────────────
    # 「を、」「に、」「で、」「も、」「へ、」「や、」
    text = re.sub(r'([をにもへや])、', r'\1', text)

    # 「が、」── 逆接「〜が、」は残したいが助詞用法の「が、」は除去
    # 「〜ことが、」「〜ものが、」など体言+が のパターンを対象に
    text = re.sub(r'(こと|もの|ところ|ため|はず|わけ|よう)が、', r'\1が', text)

    # 「の、」── 「〜ことの、」など連体修飾の「の」直後
    text = re.sub(r'(こと|もの)の、', r'\1の', text)

    # ── 2. 全角句読点の表記ゆれを統一 ────────────────────────
    text = text.replace('．', '。')
    text = text.replace('，', '、')
    text = text.replace('｡', '。')
    text = text.replace('､', '、')

    return text


# ── テスト ────────────────────────────────────────────────────
if __name__ == '__main__':
    samples = [
        "星の配置を、西洋占星術とインド占星術の両面から深く読み解き",
        "現実世界で形にし、周囲に影響を与えながら",          # 「し、」は自然なので残す
        "ことが、多いでしょう",
        "配置を，西洋占星術",                                  # 全角カンマ
        "あなたが何か新しいプロジェクトを立ち上げる際、そのアイデアは",  # 「際、」は自然
        "感情をあまり表に出さず、常に一歩引いた",             # 「ず、」は自然
        "ことの、詳細な",
        "ものが、あります",
    ]

    for s in samples:
        fixed = fix_punctuation(s)
        changed = "→ 変更" if fixed != s else "  (変更なし)"
        print(f"{changed}")
        print(f"  before: {s}")
        if fixed != s:
            print(f"  after:  {fixed}")
        print()
